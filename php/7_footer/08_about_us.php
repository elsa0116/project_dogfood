						由於寵物對於人類而言已經逐漸不再是具有上下關係的「寵物─主人」模式，而是
						視為家人的對等關係，而這樣的新型觀念之下，多數的飼主會以體貼愛護家人的
						心情，等量甚至加倍地對待寶貝寵物，也因此寵物所衍生的商機也銳不可檔，舉凡
						生活用品、服飾配件、寵物醫療保健、美容、訓練，甚至寵物後事也形成商機。
						我們之所以選擇健康狗食作為主要服務項目，主要乃是當前寵物市場中，就寵物
						的飼養類型上，仍以寵物狗為大宗(佔51.7%)。而其中消費的品項中，以食品為主要
						品項(佔80%)，而且『吃』對於寵物狗而言，是飼主對於寵物本身最直接表達愛與友好
						的方式之一。爰此，逗狗福(Dog Food)的成立，意味著DOG有福了………
